func TestListAvailableSlots_DefaultWindowAndWorkingHours(t *testing.T) {
	now := time.Date(2026, 3, 31, 6, 0, 0, 0, time.UTC)
	svc := NewCalendarService(func() time.Time { return now })

	slots, err := svc.ListAvailableSlots("name-owner", "meeting-15", nil, nil)
	if err != nil {
		t.Fatalf("expected no error, got %v", err)
	}

	if len(slots) == 0 {
		t.Fatalf("expected non-empty slots")
	}

	first := slots[0]
	if !first.StartAt.Equal(now) {
		t.Fatalf("expected first slot to start at %s, got %s", now, first.StartAt)
	}

	if !bool(first.IsAvailable) {
		t.Fatalf("expected first slot to be available")
	}
}

func TestCreateBooking_RejectsConflictsAcrossEventTypes(t *testing.T) {
	now := time.Date(2026, 3, 31, 6, 0, 0, 0, time.UTC)
	svc := NewCalendarService(func() time.Time { return now })

	_, err := svc.CreateBooking("name-owner", CreateBookingInput{
		EventTypeID: "meeting-15",
		SlotStartAt: now,
		GuestName:   "Ivan",
		GuestEmail:  "ivan@example.com",
	})
	if err != nil {
		t.Fatalf("expected first booking success, got %v", err)
	}

	_, err = svc.CreateBooking("name-owner", CreateBookingInput{
		EventTypeID: "meeting-30",
		SlotStartAt: now,
		GuestName:   "Petr",
		GuestEmail:  "petr@example.com",
	})
	if err == nil {
		t.Fatalf("expected slot conflict error")
	}

	svcErr, ok := err.(*ServiceError)
	if !ok {
		t.Fatalf("expected ServiceError, got %T", err)
	}

	if svcErr.Code != "SLOT_CONFLICT" {
		t.Fatalf("expected SLOT_CONFLICT, got %s", svcErr.Code)
	}
}

func TestCreateEventType_StoresAndReturnsNewEventType(t *testing.T) {
	now := time.Date(2026, 3, 31, 6, 0, 0, 0, time.UTC)
	svc := NewCalendarService(func() time.Time { return now })

	explicitID := "meeting-45"

	created, err := svc.CreateEventType(CreateEventTypeInput{
		ID:              &explicitID,
		Name:            "Встреча 45 минут",
		Description:     "Расширенная встреча",
		DurationMinutes: 45,
	})
	if err != nil {
		t.Fatalf("expected no error, got %v", err)
	}

	if created.Id != "meeting-45" {
		t.Fatalf("expected id meeting-45, got %s", created.Id)
	}

	types, err := svc.ListEventTypes("name-owner")
	if err != nil {
		t.Fatalf("expected no error on list, got %v", err)
	}

	found := false
	for _, eventType := range types {
		if eventType.Id == "meeting-45" {
			found = true
			break
		}
	}

	if !found {
		t.Fatalf("expected created event type in list")
	}
}

